    private void jButtonSalvarContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarContaActionPerformed
        this.contaController.cadastrarConta(
                jTextFieldContasCadastrarTitular.getText(), 
                jTextFieldContasCadastrarCpf.getText()
        );
        jComboBoxContasEditarConta.addItem(this.banco.getNumeroContasRegistradas()+"");
        
        JOptionPane.showMessageDialog(null, "Conta cadastrada com sucesso!");
        
        this.limparCadastroConta();
        
    }//GEN-LAST:event_jButtonSalvarContaActionPerformed

